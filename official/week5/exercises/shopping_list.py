### Exercise: Update Shopping List

# Objective: Create a program to update a shopping list by replacing, adding, or removing items.

# --------------------------

# Instructions:
# 1. Start with a predefined list of shopping items, e.g., ['apples', 'bananas', 'carrots', 'dates'].
# 2. Ask the user if they want to replace, add, or remove an item.
# 3. If 'replace', ask for the index of the item to replace and the new item's name, then update the list.
# 4. If 'add', ask for the new item's name and append it to the list.
# 5. If 'remove', ask for the index of the item to remove, then remove that item from the list.
# 6. Print the updated list.

# Notes:
# - Make sure to validate the user's input to ensure the index is within the bounds of the list.
# - Inform the user and ask for input again if an invalid action or index is provided.

# Example output:
# Original list: ['apples', 'bananas', 'carrots', 'dates']
# What action would you like to take (replace, add, remove)? replace
# Which index would you like to replace? 1
# What is the new item? 'berries'
# Updated list: ['apples', 'berries', 'carrots', 'dates']
