### Exercise 2: Times Table Generator

# Objective: Create a program that generates and prints a times table for a number provided by the user.

# --------------------------

# Instructions:
# 1. Ask the user to enter a number for which they want the times table.
# 2. Use a for loop to multiply the number by each number from 1 to 10.
# 3. Print each step of the multiplication in the format: X * Y = Z.

# Notes:
# - Validate that the user input is a number.

# Example Output:
# Enter a number for the times table: 3
# 3 * 1 = 3
# 3 * 2 = 6
# ...
# 3 * 10 = 30
