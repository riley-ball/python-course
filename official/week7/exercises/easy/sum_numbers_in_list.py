### Exercise 3: Sum of Numbers in a List

# Objective: Create a program that calculates the sum of numbers in a list using a for loop.

# --------------------------

# Instructions:
# 1. Create a list of numbers, e.g., [4, 5, 6, 7].
# 2. Initialize a variable 'total' to 0 before the loop.
# 3. Use a for loop to add each number in the list to 'total'.
# 4. After the loop, print the total sum.

# Notes:
# - This exercise demonstrates how to accumulate a value in a loop.

# Example Output:
# The sum of the numbers in the list is 22.
