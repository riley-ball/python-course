### Exercise 1: Basic Countdown Timer

# Objective: Create a simple countdown timer that counts down from a given number to 0.

# --------------------------

# Instructions:
# 1. Ask the user to enter a positive number to start the countdown.
# 2. Use a while loop to count down from this number to 0.
# 3. Print each number in the countdown each second.

# Notes:
# - You can use the time.sleep(1) function from the time module to wait for one second between prints.

# Example Output:
# Enter a countdown start number: 5
# 5
# 4
# 3
# 2
# 1
# 0
# Countdown finished!
