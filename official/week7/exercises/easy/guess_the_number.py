### Exercise 2: Guess the Number

# Objective: Create a 'Guess the Number' game where the user has to guess a secret number.

# --------------------------

# Instructions:
# 1. Set a variable 'secret_number' to any number of your choice (e.g., 7).
# 2. Use a while loop to repeatedly ask the user to guess the number.
# 3. The loop should continue until the user guesses the correct number.
# 4. Once the correct number is guessed, print a congratulatory message.

# Notes:
# - You can enhance this game by giving hints like "too high" or "too low".

# Example Output:
# Guess the secret number: 3
# Wrong, try again!
# Guess the secret number: 5
# Wrong, try again!
# Guess the secret number: 7
# Congratulations, you guessed it!
