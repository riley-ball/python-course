### Exercise 1: Positive Number Accumulator

# Objective: Create a program that continuously adds numbers entered by the user until they enter a negative number.

# --------------------------

# Instructions:
# 1. Initialize a variable to 0 to keep track of the total sum.
# 2. Use a while loop to prompt the user to enter a number continuously.
# 3. Add the number to the total sum if it is positive.
# 4. Break the loop if the user enters a negative number.
# 5. After exiting the loop, print the total sum.

# Notes:
# - Make sure to convert the user input to a number before adding it to the sum.

# Example Output:
# Enter a number (negative number to stop): 5
# Enter a number (negative number to stop): 10
# Enter a number (negative number to stop): -1
# Total sum: 15
