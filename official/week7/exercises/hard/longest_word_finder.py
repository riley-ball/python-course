### Exercise 2: Longest Word Finder

# Objective: Write a program to find the longest word in a sentence provided by the user.

# --------------------------

# Instructions:
# 1. Ask the user to input a sentence.
# 2. Split the sentence into a list of words.
# 3. Determine the longest word in the list.
# 4. Print the longest word and its length.

# Notes:
# - If there are multiple words of the same longest length, print the first one encountered.
# - Consider how to handle punctuation.

# Example Output:
# Enter a sentence: The quick brown fox.
# Longest word: quick, Length: 5
