### Exercise 1: List Reverser

# Objective: Create a program that reverses the order of elements in a list using a for loop.

# --------------------------

# Instructions:
# 1. Start with a predefined list of elements, e.g., [1, 2, 3, 4, 5].
# 2. Create an empty list to hold the reversed elements.
# 3. Use a for loop to iterate through the original list and add each element to the new list in reverse order.
# 4. Print the reversed list.

# Notes:
# - Do not use the built-in reverse() method or [::-1] slicing.
# - Focus on logic to manually reverse the list.

# Example Output:
# Original list: [1, 2, 3, 4, 5]
# Reversed list: [5, 4, 3, 2, 1]
