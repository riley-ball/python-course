### Exercise 1: List Element Counter

# Objective: Create a program that counts the occurrences of a specific element in a list.

# --------------------------

# Instructions:
# 1. Start with a predefined list of elements, e.g., ['apple', 'banana', 'apple', 'pear', 'banana', 'apple'].
# 2. Ask the user for an element to search for in the list.
# 3. Use a for loop to count how many times that element appears in the list.
# 4. Print the count.

# Notes:
# - Focus on case-sensitive matching.

# Example Output:
# List: ['apple', 'banana', 'apple', 'pear', 'banana', 'apple']
# Enter an element to count: apple
# The element 'apple' appears 3 times in the list.
